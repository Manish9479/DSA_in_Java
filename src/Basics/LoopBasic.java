package Basics;

public class LoopBasic {
    public static void main(String[] args) {


        int count = 1;
        while (count != 5) {
            System.out.println(count);
            count++;

        }

        //for loop
        System.out.println("Using for loop");
        for (int Count = 1; Count !=12  ; Count++){
            System.out.println(Count);
        }
    }
}
